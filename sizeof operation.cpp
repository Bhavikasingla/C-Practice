#include<iostream>      
using namespace std;
int main()
{
	cout<<"the size of int is: "<<sizeof(int)<<" bytes"<<endl;
	cout<<"the size of float is: "<<sizeof(float)<<" bytes"<<endl;
	cout<<"the size of char is: "<<sizeof(char)<<" bytes"<<endl;
	cout<<"the size of double is: "<<sizeof(double)<<" bytes"<<endl;
	return 0;
}